
#include <iostream>
using namespace std;
int main()
{
    // integer datatype
    int apples = 5;
    cout << apples << endl;
    cout << "size of Int : " << sizeof(apples) << endl;

    // character datatype
    char sign = '&';
    cout << sign << endl;
    cout << "size of Char : " << sizeof(sign) << endl;

    // boolean datatype
    bool flag = true;
    cout << flag << endl;
    cout << "size of Bool : " << sizeof(flag) << endl;

    // float datatype
    float score = 3.14;
    cout << score << endl;
    cout << "size of Float : " << sizeof(score) << endl;

    return 0;
}